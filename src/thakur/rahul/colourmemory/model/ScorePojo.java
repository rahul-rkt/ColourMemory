
package thakur.rahul.colourmemory.model;

/**
 * Plain Old Java Object for individual score entries.
 *
 * @author rahulthakur
 */
public class ScorePojo {

	private String name;
	private int score;

	public String getName() {

		return name;
	}

	public void setName(String name) {

		this.name = name;
	}

	public int getScore() {

		return score;
	}

	public void setScore(int score) {

		this.score = score;
	}
}
